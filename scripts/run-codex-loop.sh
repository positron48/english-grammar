#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è –±–µ—Å–∫–æ–Ω–µ—á–Ω–æ–≥–æ –∑–∞–ø—É—Å–∫–∞ codex exec —Å –ø–∞—É–∑–æ–π –º–µ–∂–¥—É –∑–∞–ø—É—Å–∫–∞–º–∏
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./scripts/run-codex-loop.sh

# –¶–≤–µ—Ç–æ–≤—ã–µ –∫–æ–¥—ã ANSI
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
